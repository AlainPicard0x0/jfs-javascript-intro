let item = 'shoes';
switch (item) {
    case 'shoes':
        console.log(`${item} are $50`);
        break;
    case 'jeans':
        console.log(`${item} are $25`);
        break;
    case 'hat':
        console.log(`${item} are $12`);
        break;
    case 'socks':
        console.log(`${item} are $2`);
        break;
    default:
        console.log(`invalid item`)
}
